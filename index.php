        var funcConvertData = function(poData)
        {
            var oMerge = [];
            for (var nLoop = 0; nLoop < poData.length; nLoop++)
            {
                var poGeoCoord = mapGeoCoordMap[poData[nLoop].name];
                if (poGeoCoord)
                {
                    oMerge.push
                    ({
                        name: poData[nLoop].name,
                        value: poGeoCoord.concat(poData[nLoop].value)
                    });
                }
            }
            return oMerge;
        };
