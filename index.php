    function AnsyRefresh(type)
    {
        SetFullMask('地区数据更新中，请稍候');
        $.ajax
        ({
            url		:	'./getData.php?type='+type,
            async	:	true,
            cache	:	false,
            type	:	'GET',
            success	:	function(oRsp)
            {
                var oRspJson;
                try
                {
                    oRspJson = JSON.parse(oRsp);
                }
                catch (eErr)
                {
                    oRspJson = {success:false,error:'后台返回数据解析失败'};
                }
                if (oRspJson.success == false)
                    console.log(oRspJson.error);
                else
                {
                    //转换数据
                    var oMapData = [];
                    for (var strKey in oRspJson.data)
                        oMapData.push({name:strKey,value:oRspJson.data[strKey]});
                    ShowChart($('#main')[0],oMapData);
                }
//                if(type == 1){
//                    setTimeout(AnsyRefresh(type),5000);
//                }
                HideFullMask();
            },
            error: function(oRsp)
            {
                HideFullMask();
                console.log(oRsp.status + ':' + oRsp.statusText);
//                setTimeout(AnsyRefresh(type),5000);
            }
        });
    }

